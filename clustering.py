def dist(x, y):
    """
    Euclidean distance between vectors x, y
    :param x: numpy array of size n
    :param y: numpy array of size n
    :return: the Euclidean distance
    """
    return sum([n1[i]**2 - n2[i]**2 for n1, n2, i in enumerate(zip(x, y))])**0.5


